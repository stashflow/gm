import type { Exercise, ExerciseType, Lesson, Level } from "../types";

type Skill = "vocabulary" | "grammar" | "listening" | "speaking" | "conversation" | "review";

type UnitSpec = {
  unit: string;
  level: "A1" | "A2";
  course: string;
  title: string;
  goal: string;
  focus: string[];
  skill: Skill;
  phrase: string;
  meaning: string;
  pronunciation: string;
  grammar: string;
  grammarExample: string;
  checkPrompt: string;
  checkAnswer: string;
  choices: string[];
  builder: string[];
  replyPrompt: string;
  replyAnswer: string;
  replyChoices: string[];
  localObjective: string;
  localPersona: string;
  localTarget: string;
};

type Draft = Omit<Exercise, "id" | "level">;

const toExercise = (lessonNumber: number, level: Level, index: number, draft: Draft): Exercise => ({
  id: `l${lessonNumber}-e${index + 1}`,
  level,
  ...draft,
});

const clean = (word: string) => word.replace(/[.,!?]/g, "");

const newPhrase = (spec: UnitSpec): Draft => ({
  type: "teach",
  title: "New phrase",
  prompt: "Listen and read.",
  de: spec.phrase,
  en: spec.meaning,
  tts: spec.phrase,
  tags: [spec.course, spec.skill, ...spec.focus],
  note: "Say it once.",
});

const quickChoice = (spec: UnitSpec, type: ExerciseType = "match"): Draft => ({
  type,
  title: "Choose",
  prompt: spec.checkPrompt,
  de: spec.phrase,
  en: spec.meaning,
  tts: spec.phrase,
  answer: spec.checkAnswer,
  options: spec.choices,
  tags: [spec.course, spec.skill, ...spec.focus],
});

const fillBlank = (spec: UnitSpec): Draft => ({
  type: "gap",
  title: "Fill the blank",
  prompt: spec.checkPrompt,
  de: spec.phrase,
  en: spec.meaning,
  tts: spec.phrase,
  answer: spec.checkAnswer,
  options: spec.choices,
  tags: [spec.course, "grammar", ...spec.focus],
});

const wordOrder = (spec: UnitSpec): Draft => ({
  type: "builder",
  title: "Put it in order",
  prompt: "Build the German sentence.",
  de: spec.phrase,
  en: spec.meaning,
  tts: spec.phrase,
  answer: spec.builder,
  options: [...spec.builder].sort((a, b) => clean(a).localeCompare(clean(b))),
  tags: [spec.course, "word order", ...spec.focus],
});

const listenAndSay = (spec: UnitSpec): Draft => ({
  type: "listenRepeat",
  title: "Listen and say",
  prompt: "Tap listen. Say it out loud.",
  de: spec.phrase,
  en: spec.meaning,
  tts: spec.phrase,
  pronunciation: spec.pronunciation,
  tags: [spec.course, "pronunciation", ...spec.focus],
});

const grammarTip = (spec: UnitSpec): Draft => ({
  type: "teach",
  title: "Quick tip",
  prompt: spec.grammar,
  de: spec.grammarExample,
  en: spec.meaning,
  tts: spec.grammarExample,
  tags: [spec.course, "grammar", ...spec.focus],
  note: "Look at the example, then continue.",
});

const dialogue = (spec: UnitSpec): Draft => ({
  type: "dialogue",
  title: "Reply",
  prompt: spec.replyPrompt,
  de: spec.replyAnswer,
  en: "Choose the best reply.",
  tts: spec.replyAnswer,
  answer: spec.replyAnswer,
  options: spec.replyChoices,
  tags: [spec.course, "conversation", ...spec.focus],
});

const localText = (spec: UnitSpec): Draft => ({
  type: "localText",
  title: "Text with a local",
  prompt: spec.localObjective,
  de: spec.localTarget,
  en: "Use German to get the answer.",
  tts: spec.localTarget,
  answer: spec.localTarget,
  tags: [spec.course, "conversation", "local text", ...spec.focus],
  objective: spec.localObjective,
  persona: spec.localPersona,
  targetAnswer: spec.localTarget,
  note: "Ask in German. Use IDK if you get stuck.",
});

const makeLesson = (spec: UnitSpec, index: number): Lesson => {
  const number = index + 1;
  const articleLike = spec.focus.includes("articles") || spec.focus.includes("case");
  return {
    id: `lesson-${number}`,
    number,
    level: spec.level,
    unit: spec.unit,
    title: spec.title,
    goal: spec.goal,
    focus: spec.focus,
    exercises: [
      newPhrase(spec),
      quickChoice(spec, articleLike ? "article" : "match"),
      fillBlank(spec),
      wordOrder(spec),
      listenAndSay(spec),
      grammarTip(spec),
      dialogue(spec),
      localText(spec),
    ].map((draft, draftIndex) => toExercise(number, spec.level, draftIndex, draft)),
  };
};

