import type { Exercise, ExerciseType, Lesson, Level } from "../types";

type UnitSpec = {
  unit: string;
  level: "A1" | "A2";
  title: string;
  goal: string;
  focus: string[];
  concept: string;
  de: string;
  en: string;
  pronunciation: string;
  question: string;
  answer: string;
  options: string[];
  builder: string[];
  localObjective: string;
  localPersona: string;
  localTarget: string;
};

type Draft = Omit<Exercise, "id" | "level">;

const toExercise = (lessonNumber: number, level: Level, index: number, draft: Draft): Exercise => ({
  id: `l${lessonNumber}-e${index + 1}`,
  level,
  ...draft,
});

const clean = (word: string) => word.replace(/[.,!?]/g, "");

const teach = (spec: UnitSpec): Draft => ({
  type: "teach",
  title: "Concept",
  prompt: spec.concept,
  de: spec.de,
  en: spec.en,
  tts: spec.de,
  tags: spec.focus,
  note: "GM teaches the pattern, then makes you retrieve it in different ways so it moves from recognition to usable memory.",
});

const listen = (spec: UnitSpec): Draft => ({
  type: "listenRepeat",
  title: "Listen, say, check",
  prompt: "Hear the sentence, say it out loud, then check the pronunciation note.",
  de: spec.de,
  en: spec.en,
  tts: spec.de,
  pronunciation: spec.pronunciation,
  tags: ["pronunciation", ...spec.focus],
});

const choose = (spec: UnitSpec, type: ExerciseType = "gap"): Draft => ({
  type,
  title: type === "article" ? "Article lock-in" : "Meaning in context",
  prompt: spec.question,
  de: spec.de,
  en: spec.en,
  tts: spec.de,
  answer: spec.answer,
  options: spec.options,
  tags: spec.focus,
  note: `Target: ${spec.answer}`,
});

const build = (spec: UnitSpec): Draft => ({
  type: "builder",
  title: "Build the sentence",
  prompt: "Tap the words in German word order.",
  de: spec.de,
  en: spec.en,
  tts: spec.de,
  answer: spec.builder,
  options: [...spec.builder].sort((a, b) => clean(a).localeCompare(clean(b))),
  tags: ["word order", ...spec.focus],
});

const recall = (spec: UnitSpec): Draft => ({
  type: "recall",
  title: "Active recall",
  prompt: `Choose the German for: ${spec.en}`,
  de: spec.de,
  en: spec.en,
  tts: spec.de,
  answer: spec.de,
  options: [spec.de, spec.options[0], spec.options[1]].filter(Boolean),
  tags: ["retrieval", ...spec.focus],
});

const localText = (spec: UnitSpec): Draft => ({
  type: "localText",
  title: "Text with a local",
  prompt: spec.localObjective,
  de: spec.localTarget,
  en: "Complete the texting objective in German.",
  tts: spec.localTarget,
  answer: spec.localTarget,
  tags: ["conversation", "local text", ...spec.focus],
  objective: spec.localObjective,
  persona: spec.localPersona,
  targetAnswer: spec.localTarget,
  note: "Use German only. If you are stuck, use IDK to get a tiny hint instead of switching to English.",
});

const makeLesson = (spec: UnitSpec, index: number): Lesson => {
  const number = index + 1;
  const specialType: ExerciseType =
    spec.focus.includes("articles") || spec.focus.includes("case") ? "article" : spec.focus.includes("football") ? "football" : "gap";

  return {
    id: `lesson-${number}`,
    number,
    level: spec.level,
    unit: spec.unit,
    title: spec.title,
    goal: spec.goal,
    focus: spec.focus,
    exercises: [teach(spec), listen(spec), choose(spec, specialType), build(spec), recall(spec), localText(spec)].map((draft, draftIndex) =>
      toExercise(number, spec.level, draftIndex, draft),
    ),
  };
};

